    float prix(String mat){
        float prix = 0;        
        try {
            Connect();
            res = statement.executeQuery("select * from voiture where mat = '" + mat + "'");
            if (res.next()) {
                prix = Float.parseFloat(res.getString("prix"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Location.class.getName()).log(Level.SEVERE, null, ex);
        }
        return prix;
    }
    
    float calculPenalite(String date, String mat, int duree){
        try {
            SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            LocalDate dateDebut = df.parse(date).toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            long nbJour = ChronoUnit.DAYS.between(dateDebut,new Date().toInstant().atZone(ZoneId.systemDefault()).toLocalDate());
            if (nbJour>(long) duree) {
                float prix = prix(mat);
                return (float) prix*(nbJour-(long)duree)*2;
            } else {
                return 0;
            }
            
        } catch (ParseException ex) {
            Logger.getLogger(Location.class.getName()).log(Level.SEVERE, null, ex);
        }
        return 0;
    }
    
