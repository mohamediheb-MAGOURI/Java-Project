    private int periodeSupp(String mat, float pena){
        int duree=0;
        try {
            float prix=1;
            res = statement.executeQuery("select * from voiture where mat = '"+mat+"'");
            if (res.next()){
                prix = Float.parseFloat(res.getString("prix"));   
            }
            duree = (int)(pena/(prix*2));
        } catch (SQLException ex) {
            Logger.getLogger(Location.class.getName()).log(Level.SEVERE, null, ex);
        }
        return duree;
    }
    
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // finir location
        String cin = cinField.getText();
        String mat = matField.getText();
        String dte = dteField.getText();
        if (cin.isEmpty()){
            JOptionPane.showMessageDialog(null, "veuillez sélectionner une location en cours !");
        }
        else{
            try {
                Connect();
                statement.executeUpdate("update location set etat = 'FINIE', duree = duree + "+ periodeSupp(mat,Float.parseFloat(penaliteField.getText()))+" where cin = "+cin+"and mat = '"+mat+"' and dtedeb = to_date('"+dte+"','yyyy-mm-dd')");
                statement.executeUpdate("update voiture set disp = 'DISPONIBLE' where mat = '"+mat+"'");
                JOptionPane.showMessageDialog(null, "Location finie avec succes.");
                table(locationTable,"select * from location order by id asc");
                tablePenality(penaliteTable);
                connection.close();
            } catch (SQLException ex) {
                Logger.getLogger(Location.class.getName()).log(Level.SEVERE, null, ex);
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null, "Champ invalide!!");
            }
            viderChamp();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

