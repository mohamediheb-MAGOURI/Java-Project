    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // ajouter
        String log = loginField.getText().toUpperCase();
        String mdp = mdpField.getText();
        String priv = privField.getSelectedItem().toString();
        if (log.isEmpty() || mdp.isEmpty() || priv.isEmpty()){
            JOptionPane.showMessageDialog(null, "Champ vide!!");
        }
        else{
            try {
                Connect();
                statement.executeUpdate("insert into users values ('"+log+"','"+mdp+"','"+priv+"')");
                JOptionPane.showMessageDialog(null, "Utilisateur ajouté avec succes.");
                connection.close();
            } catch (SQLException ex) {
                Logger.getLogger(PartieUser.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Problème d'ajout d'utilisateur!!");
            }
        }
        table(jTable1,"select * from users");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // supprimer
        String log = loginField.getText().toUpperCase();
        if (log.isEmpty()){
            JOptionPane.showMessageDialog(null, "Veuillez entrer un login pour le supprimer!!");
        }
        else{
            try {
                Connect();
                res = statement.executeQuery("select * from users where login = '"+log+"'");
                String priv = "";
                if (res.next()) {
                    priv=res.getString("pre");
                    if (priv.equals("true")) {
                        JOptionPane.showMessageDialog(null, "Impossible de supprimer un super utilisateur!!");
                    } else {
                        statement.executeUpdate("delete from users where login = '" + log + "'");
                        JOptionPane.showMessageDialog(null, "Utilisateur supprimé avec succes");
                        table(jTable1, "select * from users");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Utilisateur inexistant!!");                        
                }
                connection.close();
            } catch (SQLException ex) {
                Logger.getLogger(PartieUser.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

