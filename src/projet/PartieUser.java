    private void table(javax.swing.JTable tab, String sql){
        String [] t = {"LOGIN","PASSWORD","PRIVILEGE"};
        String [] inserer = new String [3];
        DefaultTableModel model = new DefaultTableModel(null,t);
        try{
            Connect();
            res = statement.executeQuery(sql);
            while(res.next()){
                inserer[0] = res.getString("login");
                inserer[2] = res.getString("pre");
                if (inserer[2].equals("true")){inserer[1] = "*****";}
                else {inserer[1] = res.getString("passwd");}
                model.addRow(inserer);
            }
            tab.setModel(model);
            connection.close();
        }
        catch(SQLException e){
            e.printStackTrace();
        }
    }
    
