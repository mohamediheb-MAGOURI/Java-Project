    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // recherche
        try{
            String sql = new SuperUtilisateur().requeteRechercheClient(cinField.getText(), prenomField.getText().toUpperCase(), nomField.getText().toUpperCase(), numeroField.getText(), permisField.getText(), nblField.getText());
            if (sql.endsWith("and ")){
                sql = sql.substring(0, sql.length()-5);
                //JOptionPane.showMessageDialog(null, sql);
                table(jTable1,sql);
            }
            else {
                JOptionPane.showMessageDialog(null, "Critere de recherche vide.", "Erreur", HEIGHT);
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Champ invalide!!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

