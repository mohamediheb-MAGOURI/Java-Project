    public void table(javax.swing.JTable tab, String sql){
        String [] client = {"CIN","PRENOM","NOM","NUMERO","NPERMIS","NBLOC"};
        String [] inserer = new String [6];
        DefaultTableModel model = new DefaultTableModel(null,client);
        try{
            Connect();
            res = statement.executeQuery(sql);
            while(res.next()){
                inserer[0] = res.getString("cin");
                inserer[1] = res.getString("prenom");
                inserer[2] = res.getString("nom");
                inserer[3] = res.getString("numero");
                inserer[4] = res.getString("npermis");
                inserer[5] = res.getString("nbloc");
                model.addRow(inserer);
            }
            tab.setModel(model);
            connection.close();
        }
        catch(SQLException e){
            e.printStackTrace();
        }
    }
    
    
