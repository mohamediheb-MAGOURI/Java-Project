    private void cinFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cinFieldActionPerformed
        // afficher si le client existe
        cinField.addActionListener((ActionEvent e) -> {
            try {
                Connect();
                String sql = "select * from clients where cin = "+cinField.getText();
                res = statement.executeQuery(sql);
                if (!res.next()){
                    prenomField.setText("Client inexistant");
                    nomField.setText("Client inexistant");
                    numeroField.setText("Client inexistant");
                    permisField.setText("Client inexistant");
                    nlocField.setText("Client inexistant");
                }
                else{                
                    prenomField.setText(res.getString("prenom"));
                    nomField.setText(res.getString("nom"));
                    numeroField.setText(res.getString("numero"));
                    permisField.setText(res.getString("npermis"));
                    nlocField.setText(res.getString("nbloc"));
                }
                connection.close();
            } catch (SQLException ex) {
                Logger.getLogger(PartieVoiture.class.getName()).log(Level.SEVERE, null, ex);
            }
        });
    }//GEN-LAST:event_cinFieldActionPerformed

