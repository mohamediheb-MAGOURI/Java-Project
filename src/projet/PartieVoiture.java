    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // bouton louer
        try {
            SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String dtDeb = df.format(jDateChooser1.getDate());
            String cinF = cinField.getText();
            String mat = matField4.getText();
            String chequeF = chequeField.getText();
            String dureeF = jTextField3.getText();
            if(dtDeb.isEmpty() || cinF.isEmpty() || mat.isEmpty() || chequeF.isEmpty() || dureeF.isEmpty()){
                JOptionPane.showMessageDialog(null, "Champ vide !!");
            }
            else{
                Connect();
                int cin = Integer.parseInt(cinF);
                float cheque = Float.parseFloat(chequeF);
                int duree = Integer.parseInt(dureeF);
                res = statement.executeQuery("select count(*) from location");
                int id = 0;
                if (res.next()) id = Integer.parseInt(res.getString("count(*)"))+1;
                String sql = "insert into location values ("+id+","+cin+",'"+mat+"',to_date('"+dtDeb+"','yyyy-mm-dd'),"+duree+","+cheque+",'EN COURS')";
                statement.executeUpdate(sql);
                
                statement.executeUpdate("update voiture set nbloc = nbloc + 1 , disp = 'LOUEE' where mat = '"+mat+"'");
                
                statement.executeUpdate("update clients set nbloc = nbloc + 1 where cin = '"+cin+"'");
                JOptionPane.showMessageDialog(null, "Location faite avec succes.");
                connection.close();
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(PartieVoiture.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Champ invalide!!");
        }
        matField4.setText("");
        marqueField4.setText("");
        modelField4.setText("");
        nbplaceField4.setText("");
        nblocField4.setText("");
        prixField4.setText("");
        cinField.setText("");
        prenomField.setText("");
        nomField.setText("");
        numeroField.setText("");
        permisField.setText("");
        nlocField.setText("");
        chequeField.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
    }//GEN-LAST:event_jButton10ActionPerformed

