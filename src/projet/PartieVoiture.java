    public void table(javax.swing.JTable tab, String sql){
        String [] voiture = {"MAT","MARQ","MOD","NB-PL","DISPO","NBLOC","PRIX/J"};
        String [] inserer = new String [7];
        DefaultTableModel model = new DefaultTableModel(null,voiture);
        try{
            Connect();
            res = statement.executeQuery(sql);
            while(res.next()){
                inserer[0] = res.getString("mat");
                inserer[1] = res.getString("marque");
                inserer[2] = res.getString("model");
                inserer[3] = res.getString("nbplace");
                inserer[4] = res.getString("disp");
                inserer[5] = res.getString("nbloc");
                inserer[6] = res.getString("prix");
                model.addRow(inserer);
            }
            tab.setModel(model);
            connection.close();
        }
        catch(SQLException e){
            e.printStackTrace();
        }
    }

